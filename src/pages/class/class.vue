<script setup lang="ts">

import { getHomeBannerAPI } from '@/services/home'
import { getCategoryTopAPI } from "@/services/class";
import { onLoad } from "@dcloudio/uni-app";
import type { BannerItem } from "@/types/home";
import type { CategoryTopItem } from "@/types/class";
import { ref, computed } from "vue";
import PageSkeleton from "./components/PageSkeleton.vue";

const bannerList = ref<BannerItem[]>([]);

const categoryList = ref<CategoryTopItem[]>([]);

const categoryNumber = ref<number>(0);

const isShow = ref<boolean>(true);

//获取轮播图数据
const getBannerData = async () => {
  const result = await getHomeBannerAPI(2)
  bannerList.value = result.result
}

//获取分类列表
const getCategoryTop = async () => {
  const result = await getCategoryTopAPI()
  categoryList.value = result.result
}

//二级分类列表
const subCategoryList = computed(() => {
    return categoryList.value[categoryNumber.value]?.children || []
})


onLoad(async () => {
  await Promise.all([ getBannerData(), getCategoryTop() ])
  isShow.value = false
})

</script>

<template>

  <PageSkeleton v-if="isShow" />

  <view class="viewport" v-else>
    <!-- 搜索框 -->
    <view class="search">
      <view class="input">
        <text class="icon-search">女靴</text>
      </view>
    </view>
    <!-- 分类 -->
    <view class="categories">
      <!-- 左侧：一级分类 -->
      <scroll-view class="primary" scroll-y>
        <view v-for="(item, index) in categoryList" :key="item.id" class="item" :class="{ active: index === categoryNumber }" @tap="categoryNumber = index">
          <text class="name"> {{ item.name }} </text>
        </view>
      </scroll-view>
      <!-- 右侧：二级分类 -->
      <scroll-view class="secondary" scroll-y>
        <!-- 焦点图 -->
        <cust-Swiper class="banner" :list="bannerList" height="160px" />
        <!-- 内容区域 -->
        <view class="panel" v-for="item in subCategoryList" :key="item.id">
          <view class="title">
            <text class="name">{{ item.name }}</text>
            <navigator class="more" hover-class="none">全部 ></navigator>
          </view>
          <view class="section">
            <navigator
              v-for="goods in item.goods"
              :key="goods.id"
              class="goods"
              hover-class="none"
              :url="`/pages/goods/goods?id=${goods.id}`"
            >
              <image
                class="image"
                :src="goods.picture"
              ></image>
              <view class="name ellipsis">{{ goods.name }}</view>
              <view class="price">
                <text class="symbol">¥</text>
                <text class="number">{{ goods.price }}</text>
              </view>
            </navigator>
          </view>
        </view>
      </scroll-view>
    </view>
  </view>
</template>

